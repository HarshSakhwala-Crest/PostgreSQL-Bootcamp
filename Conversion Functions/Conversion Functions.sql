-- TO_CHAR FUNCTION
SELECT TO_CHAR(100870, '9,99999');
" 1,00870"


SELECT release_date,
TO_CHAR(release_date, 'DD-MM-YYYY'),
TO_CHAR(release_date, 'Dy, MM, YYYY')
FROM movies;


SELECT TO_CHAR(TIMESTAMP '2020-01-01 10:30:45', 'HH24:MI:SS');
"10:30:45"


SELECT 
movie_id,
revenues_domestic,
TO_CHAR(revenues_domestic, '$999D99')
FROM movies_revenues;


-- TO_NUMBER FUNCTION
SELECT TO_NUMBER('1420.89','9999.99');
1420.89

SELECT TO_NUMBER('10,625.78-','99G999D99S');
-10625.78

SELECT TO_NUMBER('$1,420.64','L9G999D99');
1420.64

SELECT TO_NUMBER('1,234,567.89','9G999G999');
1234567

SELECT TO_NUMBER('1,234,567.89','9G999G999D99');
1234567.89

SELECT TO_NUMBER('$1,978,299.78','L9G999G999D99');
1978299.78


-- TO_DATE FUNCTION
SELECT TO_DATE('2020/10/22','YYYY/MM/DD');
"2020-10-22"

SELECT TO_DATE('022199','MMDDYY');
"1999-02-21"

SELECT TO_DATE('March 07, 2019','Month DD, YYYY');
"2019-03-07"

SELECT TO_DATE('2020/02/29','YYYY/MM/DD');
"2020-02-29"


-- TO_TIMESTAMP FUNCTION
SELECT TO_TIMESTAMP('2020-10-28 10:30:25','YYYY-MM-DD HH:MI:SS');
"2020-10-28 10:30:25+05:30"

SELECT TO_TIMESTAMP('2020     May','YYYY Month');
"2020-05-01 00:00:00+05:30"

SELECT TO_TIMESTAMP('2020-01-01 32:8:00','YYYY-MM-DD HH24:MI:SS');

SELECT TO_TIMESTAMP('2020-01-01 23:8:00','YYYY-MM-DD HH24:MI:SS');
"2020-01-01 23:08:00+05:30"